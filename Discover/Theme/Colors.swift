import UIKit

extension UIColor {
    
    static func backgroundColor() -> UIColor {
        return UIColor(red: 41/255, green: 47/255, blue: 51/255, alpha: 1.0)
    }
    
    static func navbarColor() -> UIColor {
        return UIColor(red: 46/255, green: 52/255, blue: 56/255, alpha: 1.0)
    }
    
    static func progressHUDColor() -> UIColor {
        return UIColor(red: 46/255, green: 52/255, blue: 56/255, alpha: 0.8)
    }
    
    static func buttonColor() -> UIColor {
        return UIColor(red: 162/255, green: 222/255, blue: 208/255, alpha: 0.8)
    }
    
    static func defaultButtonColor() -> UIColor {
        return UIColor(red: 0/255, green: 122/255, blue: 1/255, alpha: 1.0)
    }
}

